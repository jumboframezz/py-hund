# Проверка за отлична оценка
# Първата задача от тази тема е да се напише конзолна програма, която чете оценка (реално число), въведена
# от потребителя и отпечатва &quot;Excellent!&quot;, ако оценката е 5.50 или по-висока.
# Примерен вход
# 6
#
# изход
# Excellent!
#



score = float(input())

if score >= 5.50:
    print("Excellent!")
