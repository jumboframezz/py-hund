# Сграда
# Напишете програма, която извежда на конзолата номерата на стаите в една сграда (в низходящ ред), като са изпълнени
# следните условия:
#   На всеки четен етаж има само офиси;
#   На всеки нечетен етаж има само апартаменти;
# Всеки апартамент се означава по следния начин :
#   А{номер на етажа}{номер на апартамента}, номерата на апартаментите започват от 0;
# Всеки офис се означава по следния начин :
#   О{номер на етажа}{номер на офиса}, номерата на офисите също започват от 0;
# На последният етаж винаги има апартаменти и те са по-големи от останалите, затова пред номера им
# пише 'L', вместо 'А'. Ако има само един етаж, то има само големи апартаменти!
# От конзолата се прочитат две цели числа - броят на етажите и броят на стаите за един етаж.


def is_even(num):
    if num % 2 == 0:
        return True
    else:
        return False


debug = False
if debug:
    floors = 6
    rooms = 4
else:
    floors = int(input())
    rooms = int(input())

for floor in range(floors, 0, -1):
    if is_even(floor) and floor != floors:
        prefix = "O"
    else:
        if floor == floors or floors == 1:
            prefix = "L"
        else:
            prefix = "A"
    for room in range(rooms):
        print(f"{prefix}{floor}{room}", end=" ")
    print()
