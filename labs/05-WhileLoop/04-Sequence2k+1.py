# 4.	Редица числа 2K+1
# Напишете програма, която чете число n, въведено от потребителя, и отпечатва всички
# числа  ≤  n от редицата: 1, 3, 7, 15, 31, ….
# Всяко следващо число се изчислява като умножим предишното с 2 и добавим 1.


num = int(input())

count = 1
current = 1
while current <= num:
    print(current)
    current = current * 2 + 1
    count += 1
