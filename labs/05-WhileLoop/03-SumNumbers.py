# 3.	Сума от числа
# Напишете програма, която чете цяло число от конзолата и на всеки следващ ред цели числа, докато тяхната
# сума стане по-голяма или равна на първоначалното число. След приключване на четенето да се отпечата сумата
# на въведените числа.


stop_sum = int(input())
in_sum = 0

while in_sum < stop_sum:
    new_number = int(input())
    in_sum += new_number

print(in_sum)
