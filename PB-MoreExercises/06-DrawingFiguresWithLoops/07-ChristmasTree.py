# 7.	Коледна елха
# Напишете програма, която чете число n (1 ≤ n ≤ 100), въведено от потребителя, и печата коледна елха с
# размер n като в примерите по-долу:
#
# n = 4
#      |
#    * | *
#   ** | **
#  *** | ***
# **** | ****

n = 2
n = int(input())
print(" " * (n) + " |")
for line in range(1, n+1):
    print(" " * (n - line) + "*" * line, end="")
    print(" | ", end="")
    print("*" * line)
